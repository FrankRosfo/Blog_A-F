<a><h4>Comunicación Red</h4></a>
<p>La arquitectura de red es el diseño de una red de comunicaciones. Es un marco para la especificación de los componentes físicos de una red y de su organización funcional y configuración, sus procedimientos y principios operacionales, así como los protocolos de comunicación utilizados en su funcionamiento.
<br><br>En la telecomunicación, la especificación de un diseño de red puede incluir también una descripción detallada de los productos y servicios entregados a través de una red de comunicaciones, así como la tasa de facturación detallada y estructuras en las que se compensan los servicios. 
<br><br>El diseño de red de Internet se expresa de forma predominante por el uso de la familia de protocolos de Internet, en lugar de un modelo específico para la interconexión de redes o nodos en la red, o el uso de tipos específicos de enlaces de hardware.
<br><br>En la computación distribuida, el concepto de arquitectura de red a menudo describe la estructura y la clasificación de una arquitectura de aplicaciones distribuidas, ya que los nodos que participan en una aplicación distribuida se conocen como una red. Por ejemplo, la arquitectura de aplicaciones de la red telefónica conmutada (RTC) se ha denominado la red inteligente avanzada.
</p>
