<span>Sistemas de Informaci&oacute;n</span>
<a><h4>Deacuerdo a su Arquitectura</h4></a>
<p>Los sistemas de informaci&oacute;n, de manera general se pueden clasificar de tres formas seg&uacute;n sus
    prop&oacute;sitos generales, en este sentido Peralta (2008) clasifica los sistemas de informaci&oacute;n en
    tres tipos fundamentales:
<h6>Sistemas Transaccionales:</h6><p>Son Sistemas de Informaci&oacute;n que logran la automatizaci&oacute;n de
    procesos operativos dentro de una organizaci&oacute;n ya que su funci&oacute;n primordial consiste en procesar
    transacciones tales como pagos, cobros, entradas, salidas, etc.</p>
<h6>Sistemas de Soporte a la Toma de Decisiones:</h6><p>Son Sistemas de Informaci&oacute;n que apoyan
     el proceso de toma de decisiones.</p>
<h6>Sistemas Estrat&eacute;gicos:</h6><p>Son sistemas de informaci&oacute;n desarrollado en las organizaciones
    con el fin de lograr ventajas competitivas, a trav&eacute;s del uso de la tecnolog&iacute;a de informaci&oacute;n.
<br><br>En dependencia del enfoque (tres en total), seg&uacute;n reporta Peña (2006), los sistemas de
    informaci&oacute;n se pueden agrupar en una cierta clasificaci&oacute;n, que brinda una idea esencial de su estructura y funcionamiento.
<br><br>De acuerdo al elemento principal de proceso de la informac&oacute;n, los sistemas de informaci&oacute;n
    pueden ser de tres tipos (Manual, Mecanizadas y Bath):
</p>
<h6>Manuales:</h6><p>Cuando el hombre auxiliado por cierto equipo (m&aacute;quinas de escribir, sumadoras,
    archivos, etc.) realiza las principales funciones de recopilaci&oacute;n, registro, almacenamiento, c&aacute;lculo
    y generaci&oacute;n de informaci&oacute;n.</p>
<h6>Mecanizadas:</h6><p>Cuando cierta maquinaria realiza las principales funciones de procesamiento.
    Para los sistemas mecanizados que hacen uso de un computador, de acuerdo al tipo de interacci&oacute;n
    Hombre-M&aacute;quina, los sistemas de informaci&oacute;n pueden ser de dos tipos (Batch y en L&iacute;nea]
<br>Bratch:El usuario proporciona los datos necesarios para la ejecuci&oacute;n de un proceso y espera a
    que el computador termine la tarea para recibir los resultados.
<br>En L&iacute;nea:Existe un di&aacute;logo directo entre el usuario y el computador durante la ejecuci&oacute;n de un proceso.
<br><br>En cuanto a la organizaci&oacute;n f&iacute;sica de los principales recursos de procesamiento de datos, los
    sistemas de informaci&oacute;n pueden ser de tipo:
</p>
<h6>Procesos centralizados:</h6><p>Los recursos se encuentran ubicados en un &aacute;rea f&iacute;sica determinada,
    por lo que su acceso se realiza en las misma instalaci&oacute;n o desde lugares retirados, mediante l&iacute;neas
    de comunicaci&oacute;n de datos (telef&oacute;nicas, microondas, sat&eacute;lite, etc.).</p>
<h6>Proceso distribuido:</h6><p>Los recursos se encuentran diseminados en diversos lugares de una zona
    territorial (ciudad, pa&iacute;s, continente, etc.), por lo que el procesamiento se realiza en el propio lugar
    donde se originan los datos, existiendo la posibilidad de compartir informaci&oacute;n entre las diversas
    instalaciones, mediante la informaci&oacute;n de una “Red de Comunicaci&oacute;n”.</p>

