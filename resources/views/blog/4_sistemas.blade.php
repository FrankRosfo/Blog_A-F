<span>Sistemas</span>
<a><h4>Computadoras Centrales y Dedicadas</h4></a>
<p>El término apareció a principios de los años 70’s con la introducción de ordenadores más pequeños como la serie DEC PDP (miniordenadores) por lo que los usuarios acuñaron el término ‘ordenador central’ para describir a los tipos de ordenadores más grandes y antiguos.
<br><br>Conocidos como ‘mainframe’. Son computadoras grandes y potentes usadas por grandes compañías para el procesamiento de grandes cantidades de datos y se caracteriza por el empleo de un gran número de dispositivos de E/S y mantener una ingeniería interna redundante que resulta en alta confiabilidad y seguridad; por ejemplo, el procesamiento de transacciones bancarias. 
<br><br>Está relacionado con los usuarios a través de dispositivos menos poderosos como workstations o terminales. Implica una forma de cómputo centralizado en oposición a la forma de cómputo distribuido en la cual se usa un número masivo de ordenadores organizados en clusters o grid’s e interconectados por una red de comunicaciones para llevar a cabo una tarea informática.
<br><br>Permanecen invisibles al público en general. Actualmente IBM tiene un dominio homogénico en el mercado mundial de mainframe. 
Usualmente los manufactureros de sistemas computacionales y profesionistas de TI emplean el término “plataforma” para referirse al Hardware y al Software que están asociados con una arquitectura particular.
</p>
